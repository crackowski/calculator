//Function that adds two numbers

function add(a, b){
    return a + b;
}

//function that subtracts two numbers

function subtract(a, b){
    return a - b;
}

//Function that multiplies two numbers

function multiply(a, b){
    return a * b;
}

//Function that divides two numbers

function divide(a, b){
    return a / b;
}